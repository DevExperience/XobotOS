		DARK_GREEN = unchecked((int)(0x4)),
		DARK_CYAN = unchecked((int)(0x5)),
		DARK_BLUE = unchecked((int)(0x6)),
		DARK_MAGENTA = unchecked((int)(0x7)),
		GRAY = unchecked((int)(0x8)),
		WHITE = unchecked((int)(0x9)),
		BRIGHT_RED = unchecked((int)(0xa)),
		BRIGHT_YELLOW = unchecked((int)(0xb)),
		BRIGHT_GREEN = unchecked((int)(0xc)),
		BRIGHT_CYAN = unchecked((int)(0xd)),
		BRIGHT_BLUE = unchecked((int)(0xe)),
		BRIGHT_MAGENTA = unchecked((int)(0xf))
	}
}
