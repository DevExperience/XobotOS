using Sharpen;

namespace android.telephony.@internal.cat
{
	/// <summary>Enumeration for representing text alignment.</summary>
	/// <remarks>
	/// Enumeration for representing text alignment.
	/// <hide></hide>
	/// </remarks>
	public enum TextAlignment
	{
		LEFT = unchecked((int)(0x0)),
		CENTER = unchecked((int)(0x1)),
		RIGHT = unchecked((int)(0x2)),
		/// <summary>Language dependent (default)</summary>
		DEFAULT = unchecked((int)(0x3))
	}
}
